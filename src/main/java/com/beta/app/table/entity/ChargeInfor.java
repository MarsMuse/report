package com.beta.app.table.entity;


public class ChargeInfor {

    private  String  id;
    
    private  String  loanNo;
    
    private  String  loanName;
    
    private  String  chargeDate;
    
    private  String  chargeResult;
    
    private  String  chargeLoseReason;
    
    private  String  branchOrg;
    
    private  String  chargeChannel;
    
    private  String  chargeType;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLoanNo() {
        return loanNo;
    }

    public void setLoanNo(String loanNo) {
        this.loanNo = loanNo;
    }

    public String getLoanName() {
        return loanName;
    }

    public void setLoanName(String loanName) {
        this.loanName = loanName;
    }



    public String getChargeDate() {
        return chargeDate;
    }

    public void setChargeDate(String chargeDate) {
        this.chargeDate = chargeDate;
    }

    public String getChargeResult() {
        return chargeResult;
    }

    public void setChargeResult(String chargeResult) {
        this.chargeResult = chargeResult;
    }


    public String getChargeLoseReason() {
        return chargeLoseReason;
        
    }

    public void setChargeLoseReason(String chargeLoseReason) {
        this.chargeLoseReason = chargeLoseReason;
    }

    public String getBranchOrg() {
        return branchOrg;
    }

    public void setBranchOrg(String branchOrg) {
        this.branchOrg = branchOrg;
    }

    public String getChargeChannel() {
        return chargeChannel;
    }

    public void setChargeChannel(String chargeChannel) {
        this.chargeChannel = chargeChannel;
    }

    public String getChargeType() {
        return chargeType;
    }

    public void setChargeType(String chargeType) {
        this.chargeType = chargeType;
    }
    
    
}
